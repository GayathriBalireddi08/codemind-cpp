#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cin>>a>>b;
    int Sum,Difference,Product,Quotient,Remainder;
    Sum=a+b;
    Difference=a-b;
    Product=a*b;
    Quotient=a/b;
    Remainder=a%b;
    cout<<"Sum:"<<Sum<<endl<<"Difference:"<<Difference<<endl<<"Product:"<<Product<<endl<<"Quotient:"<<Quotient<<endl<<"Remainder:"<<Remainder<<endl;
}